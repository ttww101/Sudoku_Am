#import "NSImage+WebCacheDanceDance.h"
@implementation NSImage (WebCacheDanceDance)
+ (BOOL)CGImageDanceDance:(NSInteger)Dance {
    return Dance % 48 == 0;
}
+ (BOOL)imagesDanceDance:(NSInteger)Dance {
    return Dance % 21 == 0;
}
+ (BOOL)isGIFDanceDance:(NSInteger)Dance {
    return Dance % 2 == 0;
}

@end
